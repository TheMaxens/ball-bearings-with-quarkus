package org.dhbw.mosbach.ai;

public class Load {
    private double fr;
    private double fa;
    private double n;
    private double xr;
    private double ya;

    public Load(double fr, double fa, double n, double xr, double ya) {
        this.fr = fr;
        this.fa = fa;
        this.n = n;
        this.xr = xr;
        this.ya = ya;
    }

    public double getFr() {
        return fr;
    }

    public double getFa() {
        return fa;
    }

    public double getN() {
        return n;
    }

    public double getXr() {
        return xr;
    }

    public double getYa() {
        return ya;
    }

    
    
    
}
