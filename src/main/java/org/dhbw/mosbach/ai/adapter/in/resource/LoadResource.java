public class LoadResource {
    private double fr;
    private double fa;
    private double n;
    private double xr;
    private double ya;
    
    public double getFr() {
        return fr;
    }
    public void setFr(double fr) {
        this.fr = fr;
    }
    public double getFa() {
        return fa;
    }
    public void setFa(double fa) {
        this.fa = fa;
    }
    public double getN() {
        return n;
    }
    public void setN(double n) {
        this.n = n;
    }
    public double getXr() {
        return xr;
    }
    public void setXr(double xr) {
        this.xr = xr;
    }
    public double getYa() {
        return ya;
    }
    public void setYa(double ya) {
        this.ya = ya;
    }
    
}
